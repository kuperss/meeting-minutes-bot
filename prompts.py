"""給 NotebookLM 的 prompt 範本。

目標:讓 NotebookLM 依錄音逐字稿產出**結構化 JSON**,方便程式 parse 後寫入 Sheet。
"""
from __future__ import annotations

MEETING_MINUTES_PROMPT = """\
你是業務部會議紀錄專員。請根據會議錄音逐字稿,產生標準化會議紀錄。

## 輸出格式
嚴格只輸出一個 JSON 物件,**前後不要有任何說明文字、不要加程式碼圍欄 ```**。格式:

{
  "main_content": [
    "議題/討論/決議摘要 1",
    "議題/討論/決議摘要 2"
  ],
  "followup_items": [
    "需後續追蹤事項 1 (含負責人/期限若有提及)",
    "需後續追蹤事項 2"
  ]
}

## 規則
1. `main_content` 最多 10 條,每條 1–2 句,聚焦:
   - 關鍵議題與結論
   - 數字、客戶名、金額、產品名要保留
   - 不要流水帳,抓重點
2. `followup_items` 只列**明確的待辦/追蹤**:
   - 有明確動作(確認、聯繫、製作、送出…)
   - 如果逐字稿裡提到負責人或期限,一定要帶上(例:「小明本週五前提案」)
   - 沒有追蹤事項就回傳 `[]`
3. 全部使用繁體中文
4. 嚴格 JSON,不能有多餘逗號、註解
"""
